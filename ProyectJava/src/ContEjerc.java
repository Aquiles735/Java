public class ContEjerc {

    public static void main(String[] args) {

        int conteoReg;
// conteo regresivo en cónsola de dos en dos desde 250 hasta 200
        for (conteoReg=250; conteoReg>=200; conteoReg-=2){
            System.out.println("Conteo fín de año..."+conteoReg);
        }
        System.out.println("Feliz año nuevo...");

    }
}